    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        
        String course = txtcourse.getText();
        String Duration = txtDuration.getText();
        String Durationoptions = txtOptions.getSelectedItem().toString();
        try {      
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/studentmanagement","root","");
            
            
                    pst = con.prepareStatement("insert into courses(coursename,duration,options)values(?,?,?)");
                    pst.setString(1,course  );
                    pst.setString(2,Duration);
                    pst.setString(3,Durationoptions);
                   
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Course Created....... ");
                    txtcourse.setText("");
                    txtDuration.setText("");
                    txtOptions.setSelectedIndex(-1);
                 
                    txtcourse.requestFocus();
            
           
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Courses.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

